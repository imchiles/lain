package com.lain.entity;

public class DeviceLocationPojo {
	/**
	 * @lId 左边栏ID
	 */
	private int dlId;
	/**
	 * @lDeviceLocation 所在位置名称
	 */
	private String dlName;
	
	public int getDlId() {
		return dlId;
	}
	public void setDlId(int dlId) {
		this.dlId = dlId;
	}
	public String getDlName() {
		return dlName;
	}
	public void setDlName(String dlName) {
		this.dlName = dlName;
	}

}
