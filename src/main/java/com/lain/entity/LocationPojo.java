package com.lain.entity;

public class LocationPojo {
	
	private int elm_id;
	private String elm_name;
	private int elm_address;
	private double elm_length;
	private int elm_status;
	private int di_id;
	
	public int getDi_id() {
		return di_id;
	}
	public void setDi_id(int di_id) {
		this.di_id = di_id;
	}
	public int getElm_id() {
		return elm_id;
	}
	public void setElm_id(int elm_id) {
		this.elm_id = elm_id;
	}
	public String getElm_name() {
		return elm_name;
	}
	public void setElm_name(String elm_name) {
		this.elm_name = elm_name;
	}
	public int getElm_address() {
		return elm_address;
	}
	public void setElm_address(int elm_address) {
		this.elm_address = elm_address;
	}
	public double getElm_length() {
		return elm_length;
	}
	public void setElm_length(double elm_length) {
		this.elm_length = elm_length;
	}
	public int getElm_status() {
		return elm_status;
	}
	public void setElm_status(int elm_status) {
		this.elm_status = elm_status;
	}
	

}
